int COutcomeProbabilities::getProbWin(int nIndex)
{
    if((nIndex < 0))
        return m_ProbWin[0];

    return m_ProbWin[nIndex > 30 ? 30 : nIndex];
}

int COutcomeProbabilities::getProbDraw(int nIndex)
{
    if((nIndex < 0))
        return m_ProbWin[0];

    return m_ProbDraw[nIndex > 30 ? 30 : nIndex];
}

